def get_national_team(country_code):
    """Obtiene la selección nacional por código de país."""
    for nt in NATIONAL_TEAMS:
        if nt["country_code"] == country_code:
            return nt
    return None


def build_national_squad(country_code, all_rosters, all_teams):
    """
    Arma dinámicamente la plantilla de una selección nacional
    buscando los mejores jugadores de esa nacionalidad en el universo del juego.
    Rellena con agentes libres paródicos si no hay suficientes.
    
    Retorna: (team_data_dict, roster_list, free_agents_used)
    """
    nt = get_national_team(country_code)
    if not nt:
        return None, [], []
    
    # 1. Buscar TODOS los jugadores de esa nacionalidad en el universo
    candidates = []
    for team_short, roster in all_rosters.items():
        for player in roster:
            if player.get("nat") == country_code:
                candidates.append(player)
    
    # 2. Ordenar por OVR descendente
    candidates.sort(key=lambda p: p.get("ovr", 0), reverse=True)
    
    # 3. Armar equipo titular (11) por posiciones necesarias
    needed_positions = ["GK", "LB", "CB", "CB", "RB", "CM", "CM", "CM", "LW", "ST", "RW"]
    subs_positions = ["GK", "CB", "CB", "LB", "RB", "CM", "CAM", "ST"]
    
    squad = []
    used_names = set()
    
    def pick_best_for_pos(pos, pool):
        # Buscar exactamente esa posición primero
        for p in pool:
            if p.get("pos") == pos and p["name"] not in used_names:
                used_names.add(p["name"])
                return p
        # Fallback: posición similar
        similar = {
            "GK": ["GK"], "CB": ["CB"], "LB": ["LB", "LWB", "CB"], "RB": ["RB", "RWB", "CB"],
            "CM": ["CM", "CDM", "CAM"], "CDM": ["CDM", "CM"], "CAM": ["CAM", "CM", "CF"],
            "LW": ["LW", "LM", "ST"], "RW": ["RW", "RM", "ST"], "ST": ["ST", "CF", "LW", "RW"]
        }
        for alt_pos in similar.get(pos, [pos]):
            for p in pool:
                if p.get("pos") == alt_pos and p["name"] not in used_names:
                    used_names.add(p["name"])
                    return p
        return None
    
    # Llenar titulares
    for pos in needed_positions:
        picked = pick_best_for_pos(pos, candidates)
        if picked:
            squad.append(picked)
    
    # Llenar suplentes
    for pos in subs_positions:
        picked = pick_best_for_pos(pos, candidates)
        if picked:
            squad.append(picked)
    
    # 4. Rellenar con agentes libres reales si faltan
    free_agents_used = []
    total_needed = 19  # 11 titulares + 8 suplentes
    
    if len(squad) < total_needed:
        fa_pool = nt.get("free_agents", [])
        import copy
        for fa_template in fa_pool:
            if len(squad) >= total_needed:
                break
            if fa_template["name"] in used_names:
                continue
            
            # Crear jugador completo a partir del template
            from data.procedural import generate_player
            fa = generate_player(nt["league_pool"], fa_template["pos"], fa_template["ovr"])
            fa["name"] = fa_template["name"]
            fa["age"] = fa_template["age"]
            fa["ovr"] = fa_template["ovr"]
            fa["nat"] = country_code
            fa["is_free_agent"] = True
            fa["num"] = random.randint(1, 99)
            
            used_names.add(fa["name"])
            squad.append(fa)
            free_agents_used.append(fa)
            
    if len(squad) < total_needed:
        # Fallback defensivo para evitar caidas: generar relleno procedimental
        needed_diff = total_needed - len(squad)
        fallback_positions = ["GK", "CB", "LB", "RB", "CM", "CAM", "LW", "RW", "ST"]
        from data.procedural import generate_player
        for _ in range(needed_diff):
            pos = random.choice(fallback_positions)
            ovr = nt.get("target_ovr", 72)
            fa = generate_player(nt.get("league_pool", country_code), pos, ovr)
            fa["nat"] = country_code
            fa["is_free_agent"] = True
            fa["num"] = random.randint(2, 99)
            
            # Evitar colision de nombres
            while fa["name"] in used_names:
                fa["name"] = fa["name"] + " Jr."
                
            used_names.add(fa["name"])
            squad.append(fa)
            free_agents_used.append(fa)
    
    # Asignar números de camiseta únicos
    used_nums = set()
    for i, p in enumerate(squad):
        if i == 0 and p.get("pos") == "GK":
            p["num"] = 1
            used_nums.add(1)
        else:
            num = p.get("num", 0)
            while num in used_nums or num == 0:
                num = random.randint(2, 99)
            p["num"] = num
            used_nums.add(num)
    
    # Team data para usar en partido
    team_data = {
        "name": nt["name"],
        "short": nt["short"],
        "primary": nt["primary"],
        "secondary": nt["secondary"],
        "accent": nt["accent"],
        "badge_shape": nt.get("badge_shape", "shield"),
        "stats": {"speed": nt["target_ovr"], "shot": nt["target_ovr"],
                  "defense": nt["target_ovr"], "passing": nt["target_ovr"]},
    }
    
    return team_data, squad, free_agents_used
